    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Location  $location
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $area = Location::where('id', $id)->first();
        $area->forceDelete();

        session()->flash('Delete', 'تم حذف المنطقة');

        return back();
       
        
    }
